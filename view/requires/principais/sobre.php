     <p>A situação atual do meio ambiental e as preocupantes projeções sobre as mudanças climáticas e suas consequências no planeta, levou um grupo de jovens a se mobilizar em prol da educação ambiental e criar o Abá Ybyrá.
       <p>Abá Ybyrá são duas palavras tupis - Abá, homem, Ybyrá, árvore - que para nós significa um resgate aos habitantes originais de nossa terra e seus conhecimentos sobre preservação e vida em uníssono com o meio ambiente.</p>

       <p>Sabemos que hoje em dia existem muitas informações disponíveis sobre consequências dos nossos atos no meio ambiente, mas que, de alguma forma, elas não estão sendo efetivas para conscientizar a população a mudar seus hábitos por outros mais sustentáveis.</p>
