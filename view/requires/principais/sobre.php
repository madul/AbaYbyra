    <div class="col-sm-5   texto">

    <p>Decidimos, então, focar nos futuros líderes e tomadores de decisão da sociedade, mostrando de forma divertida e efetiva como o meio ambiente funciona e como nossas ações são importantes para preservá-lo.</p>

    <p>Por meio de materiais didáticos e atividades práticas, levamos aos alunos do ensino básico conceitos sobre o cuidado com o meio ambiente, impacto das atividades humanas no mesmo e hábitos sustentáveis que ajudam a preservá-lo.</p>

    <p>Com isso, temos como objetivo levar a consciência ambiental para as famílias por meio das crianças, tornando o presente sustentável e garantindo um futuro onde o homem e o meio ambiente vivam em harmonia.</p>


